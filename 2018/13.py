grid = [[char for char in line] for line in data]
moved = []
carts = 0

for y in range(len(grid)):
    for x in range(len(grid[y])):
        match grid[y][x]:
            case "^" | "v":
                grid[y][x] += "|l"
                carts += 1
            case "<" | ">":
                grid[y][x] += "-l"
                carts += 1


def print_grid():
    print("\n".join(["".join([char[0] for char in line]) for line in grid]))
    print()
    print("Remaining carts:", carts)
    for _ in range(5):
        print()


while True:
    for y in range(len(grid)):
        for x in range(len(grid[y])):
            pos_str = f"{x},{y}"
            if moved.count(pos_str) > 0:
                continue
            char = grid[y][x]
            on_track = None
            next_turn = None
            if len(char) > 1:
                on_track = char[1]
                next_turn = char[2]
                char = char[0]
            match char:
                case "^":
                    target_str = f"{x},{y-1}"
                    if "^v<>".count(grid[y-1][x][0]) > 0:
                        # print("Crash occurred at", target_str)
                        # exit(0)
                        grid[y][x] = on_track
                        grid[y-1][x] = grid[y-1][x][1]
                        carts -= 2
                        continue
                    match grid[y-1][x]:
                        case "|":
                            grid[y-1][x] = f"^|{next_turn}"
                        case "/":
                            grid[y-1][x] = f">/{next_turn}"
                        case "\\":
                            grid[y-1][x] = f"<\\{next_turn}"
                        case "+":
                            match next_turn:
                                case "l":
                                    grid[y-1][x] = f"<+s"
                                case "s":
                                    grid[y-1][x] = f"^+r"
                                case "r":
                                    grid[y-1][x] = f">+l"
                        case " " | "-":
                            print("Illegal move from", pos_str, "to ^")
                            exit(0)
                    grid[y][x] = on_track
                    moved.append(target_str)
                case "v":
                    target_str = f"{x},{y+1}"
                    if "^v<>".count(grid[y+1][x][0]) > 0:
                        # print("Crash occurred at", target_str)
                        # exit(0)
                        grid[y][x] = on_track
                        grid[y+1][x] = grid[y+1][x][1]
                        carts -= 2
                        continue
                    match grid[y+1][x]:
                        case "|":
                            grid[y+1][x] = f"v|{next_turn}"
                        case "/":
                            grid[y+1][x] = f"</{next_turn}"
                        case "\\":
                            grid[y+1][x] = f">\\{next_turn}"
                        case "+":
                            match next_turn:
                                case "l":
                                    grid[y+1][x] = f">+s"
                                case "s":
                                    grid[y+1][x] = f"v+r"
                                case "r":
                                    grid[y+1][x] = f"<+l"
                        case " " | "-":
                            print("Illegal move from", pos_str, "to v")
                            exit(0)
                    grid[y][x] = on_track
                    moved.append(target_str)
                case "<":
                    target_str = f"{x-1},{y}"
                    if "^v<>".count(grid[y][x-1][0]) > 0:
                        # print("Crash occurred at", target_str)
                        # exit(0)
                        grid[y][x] = on_track
                        grid[y][x-1] = grid[y][x-1][1]
                        carts -= 2
                        continue
                    match grid[y][x-1]:
                        case "-":
                            grid[y][x-1] = f"<-{next_turn}"
                        case "/":
                            grid[y][x-1] = f"v/{next_turn}"
                        case "\\":
                            grid[y][x-1] = f"^\\{next_turn}"
                        case "+":
                            match next_turn:
                                case "l":
                                    grid[y][x-1] = f"v+s"
                                case "s":
                                    grid[y][x-1] = f"<+r"
                                case "r":
                                    grid[y][x-1] = f"^+l"
                        case " " | "|":
                            print("Illegal move from", pos_str, "to <")
                            exit(0)
                    grid[y][x] = on_track
                    moved.append(target_str)
                case ">":
                    target_str = f"{x+1},{y}"
                    if "^v<>".count(grid[y][x+1][0]) > 0:
                        # print("Crash occurred at", target_str)
                        # exit(0)
                        grid[y][x] = on_track
                        grid[y][x+1] = grid[y][x+1][1]
                        carts -= 2
                        continue
                    match grid[y][x+1]:
                        case "-":
                            grid[y][x+1] = f">-{next_turn}"
                        case "/":
                            grid[y][x+1] = f"^/{next_turn}"
                        case "\\":
                            grid[y][x+1] = f"v\\{next_turn}"
                        case "+":
                            match next_turn:
                                case "l":
                                    grid[y][x+1] = f"^+s"
                                case "s":
                                    grid[y][x+1] = f">+r"
                                case "r":
                                    grid[y][x+1] = f"v+l"
                        case " " | "|":
                            print("Illegal move from", pos_str, "to >")
                            exit(0)
                    grid[y][x] = on_track
                    moved.append(target_str)
    print_grid()
    moved.clear()
